c=tf.matmul(a,b)
sess=tf.Session()
print("matmul",sess.run(c))
a=tf.constant([[1,2]])
b=tf.constant([[1,2],[2,3],[3,4]])
c=tf.multiply(a,b)
print("multiply",sess.run(c))
a=tf.constant([[1,2,3]])
